const titulo = document.querySelector(".titulo");
//[add]lo que hace es agregar una clase
titulo.classList.add("grande");

//[remove] lo que hace es remover una clase
titulo.classList.remove("grande");

//[contains]lo que hace es verificar si tiene la clas
titulo.classList.contains("grande");

//[replace]lo que hace es remplazar una clase por otra
titulo.classList.replace("grande","chico");





